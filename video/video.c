static const u32 font_data_lookup_table[16] =
  {
    0x00000000,
    0xFF000000,
    0x00FF0000,
    0xFFFF0000,
    0x0000FF00,
    0xFF00FF00,
    0x00FFFF00,
    0xFFFFFF00,
    0x000000FF,
    0xFF0000FF,
    0x00FF00FF,
    0xFFFF00FF,
    0x0000FFFF,
    0xFF00FFFF,
    0x00FFFFFF,
    0xFFFFFFFF
  };

void draw_char_to_dst(volatile uint8_t *dst, int x, int y, char c, u8 fg, u8 bg)
{
  u32 foreground = fg;
  u32 background = bg;
  u32 mask1, mask2;
  const u8 *font_data_for_char = font_8x8[(u8)c];
  u32 packed_foreground = (foreground << 24) | (foreground << 16) | (foreground << 8) | foreground;
  u32 packed_background = (background << 24) | (background << 16) | (background << 8) | background;

  int pixel = coordinate_to_pixel(x, y);
  u8 row_data;
  for (int row = 0; row < FONT_SIZE; row++)
    {
      row_data = font_data_for_char[row];
      mask1 = font_data_lookup_table[row_data >> 4];
      mask2 = font_data_lookup_table[row_data & 0x0F];
      *(u32*)(&dst[pixel]) = (packed_foreground & mask1) | (packed_background & ~mask1);
      *(u32*)(&dst[pixel + 4]) = (packed_foreground & mask2) | (packed_background & ~mask2);
      pixel += VGA_WIDTH;
    }
}

void draw_string_to_dst(volatile uint8_t *dst, int x, int y, char *s, u8 foreground, u8 background)
{
  int xx = x;
  while (*s)
    {
      if (*s == '\n') xx = x, y += FONT_SIZE;
      else
	      {
	        draw_char_to_dst(dst, xx, y, *s, foreground, background);
	        xx += FONT_SIZE;
	      }
      ++s;
    }
}

