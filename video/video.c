void copy_buffer_to_vga_memory()
{
  uint32_t *src = (uint32_t*)video_buffer;
  uint32_t *dst = (uint32_t*)VGA_MEMORY;
  for (int i = 0; i < (VGA_WIDTH * VGA_HEIGHT) / 4; ++i)
    {
      dst[i] = src[i];
    }
}
