static inline int coordinate_to_pixel(int x, int y)
{
  return y * VGA_WIDTH + x;
}

void draw_pixel_to_dst(volatile uint8_t *dst, int x, int y, u8 color)
{
  dst[coordinate_to_pixel(x, y)] = color;
}

void fill_rect_to_dst(volatile uint8_t *dst, int x, int y, int w, int h, u8 color)
{
  int pixel = coordinate_to_pixel(x, y);
  int yy = pixel;
  for (int i = 0; i < h; ++i)
    {
      pixel = yy;
      for (int j = 0; j < w; ++j)
	      {
	        dst[pixel++] = color;
	      }
      yy += VGA_WIDTH;
    }
}

void draw_line_to_dst(volatile uint8_t *dst, int x1, int y1, int x2, int y2, uint8_t color)
{
    int delta_x = x2 - x1;
    int8_t ix = (delta_x > 0) - (delta_x < 0);
    delta_x = abs(delta_x) << 1;
    int delta_y = y2 - y1;
    int8_t iy = (delta_y > 0) - (delta_y < 0);
    delta_y = abs(delta_y) << 1;

    draw_pixel_to_dst(dst, x1, y1, color);

    if (delta_x >= delta_y)
    {
        int error = delta_y - (delta_x >> 1);
        while (x1 != x2)
        {
            if ((error >= 0) && (error || (ix > 0)))
            {
                error -= delta_x;
                y1 += iy;
            }
            error += delta_y;
            x1 += ix;
            draw_pixel_to_dst(dst, x1, y1, color);
        }
    }
    else
    {
        int error = delta_x - (delta_y >> 1);
        while (y1 != y2)
        {
            if ((error >= 0) && (error || (iy > 0)))
            {
                error -= delta_y;
                x1 += ix;
            }
            error += delta_x;
            y1 += iy;
            draw_pixel_to_dst(dst, x1, y1, color);
        }
    }
}

void draw_rect_to_dst(volatile uint8_t *dst, int x, int y, int w, int h, u8 color)
{
  draw_line_to_dst(dst, x, y, x + w - 1, y, color);
  draw_line_to_dst(dst, x + w - 1, y, x + w - 1, y + h - 1, color);
  draw_line_to_dst(dst, x + w - 1, y + h - 1, x, y + h - 1, color);
  draw_line_to_dst(dst, x, y + h - 1, x, y, color);
}

